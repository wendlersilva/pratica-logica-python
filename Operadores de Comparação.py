numero1 = 20
numero2 = 10

if numero1 == numero2:
    print("Ambos os números são iguais")

if numero1 != numero2:
    print("Os números são diferentes")

if numero1 > numero2:
    print("Número 1 é maior do que o Número 2")

if numero1 >= numero2:
    print("Número 1 é maior ou igual ao Número 2")

if numero1 < numero2:
    print("Número 1 é menor que o Número 2")

if numero1 <= numero2:
    print("Número 1 é menor ou igual ao Número 2")
